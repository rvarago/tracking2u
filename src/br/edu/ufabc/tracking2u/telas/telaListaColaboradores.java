	private void carregaListaColaboradores() {
		List<? super Colaborador> listaColaboradores = new ArrayList<Colaborador>();
		try {
			listaColaboradores = this.manager.list(Colaborador.class);
		} catch (IOException ex) {
			Logger.getLogger(telaListaColaboradores.class.getName()).log(Level.SEVERE, null, ex);
		} catch (ClassNotFoundException ex) {
			Logger.getLogger(telaListaColaboradores.class.getName()).log(Level.SEVERE, null, ex);
		}
		DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
		modelo.addColumn("ID");
		modelo.addColumn("Nome");

		if (listaColaboradores.isEmpty()) {
			modelo.addRow(new String[] { "Sem dados", null });
		}
		for (int i = 0; i < listaColaboradores.size(); i++) {
			Colaborador c = (Colaborador) listaColaboradores.get(i);
			modelo.addRow(new String[] { c.getId() + "", c.getNome() });
		}
		this.tableColaboradores.setModel(modelo);

	}


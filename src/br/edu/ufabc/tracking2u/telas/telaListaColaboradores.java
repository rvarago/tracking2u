	private void buttonExcluirColaboradorActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_buttonExcluirColaboradorActionPerformed
		// TODO add your handling code here:
		try {
			int linha = this.tableColaboradores.getSelectedRow();
			Colaborador c = this.manager.find(Long.valueOf(linha + 1), Colaborador.class);
			this.manager.delete(c);
			carregaListaColaboradores();

		} catch (Exception e) {
			JOptionPane.showMessageDialog(this, "Selecione um colaborador");
		}
	}// GEN-LAST:event_buttonExcluirColaboradorActionPerformed

